package uk.co.real_logic.sbe.read_cme_pcaps.tablebulders;

public class RowCounter {
private int row_count;


    public RowCounter() {
        this.row_count = 0;
    }

    public int increment_row_count() {
        this.row_count+=0;
        return this.row_count;
    }

    public int get_row_count() {
        return this.row_count;
    }




}
